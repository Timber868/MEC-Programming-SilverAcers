    for i in range(rows):
        for j in range(cols):
            # Check if the cell is an empty corner (0)
            if matrix[i][j] == 0:
                # Check adjacent cells for colors (1-4)
                adjacent_colors = set()
                for dx, dy in [(-1, 0), (1, 0), (0, -1), (0, 1)]:
                    ni, nj = i + dx, j + dy
                    if in_bounds(ni, nj, rows, cols) and matrix[ni][nj] in [1, 2, 3, 4]:
                        adjacent_colors.add(matrix[ni][nj])

                # If there's an adjacent cell with a color, add this position to listOfCorner
                if adjacent_colors:
                    listOfCorner.append((i, j))

    return listOfCorner

