# Helper function to check if a cell is within bounds
def in_bounds(x, y, rows, cols):
    return 0 <= x < rows and 0 <= y < cols

def findCornerPos(color, matrix):
    listOfCorner = []
    rows = len(matrix)
    cols = len(matrix[0]) if rows > 0 else 0

    # Loop over each cell in the matrix
    for i in range(rows):
        for j in range(cols):
            # Check if the cell is an empty corner (0)
            if matrix[i][j] == 0:
                # Check adjacent cells for the specified color only
                valid_corner = False
                for dx, dy in [(-1, 0), (1, 0), (0, -1), (0, 1)]:
                    ni, nj = i + dx, j + dy
                    if in_bounds(ni, nj, rows, cols) and matrix[ni][nj] == color:
                        valid_corner = True
                        break  # We found a valid adjacent cell, no need to check further

                # If there's an adjacent cell with the same color, add this position to listOfCorner
                if valid_corner:
                    listOfCorner.append((i, j))

    return listOfCorner

